print(' โปรแกรมเกมทายตัวเลขที่มีค่าอยู่ที่ 1 - 100 ')
print('+++++++++++++++++++++++++++++++')
correct_number = 59
while True:
    guess = int(input("กรุณาป้อนตัวเลขที่คุณต้องการทาย (1-100): "))
    if guess == correct_number:
        print('ยินดีด้วยคุณทายถูก')
        break
    elif guess > correct_number:
        print('คุณทายผิดตัวเลขที่ป้อนมากไป')
    else:
        print('คุณทายผิดตัวเลขที่ป้อนน้อยไป')
print('+++++++++++++++++++++++++++++++')
