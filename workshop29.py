while True:
    num1 = int(input("กรุณาใส่ตัวเลขที่ 1: "))
    num2 = int(input("กรุณาใส่ตัวเลขที่ 2: "))
    ans = num1 * num2
    print(f"ผลคูณของ {num1} และ {num2} คือ {ans}")
    if ans > 5000:
        print("ผลคูณมากกว่า 5000 โปรแกรมหยุดทำงาน")
        break
